    private void deleteMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMenuActionPerformed
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus buku?", "Hapus Data Buku", dialogButton);
        if(dialogResult == 0) {
            Statement s;
            try {
                s = (Statement) obj.createStatement();
                s.executeUpdate(
                    "delete from buku where kode = '"+
                        tabelDataBuku.getValueAt(tabelDataBuku.getSelectedRow(), 1).toString()+"'");
                JOptionPane.showMessageDialog(null, "Berhasil Menghapus Buku!");
                
                this.tampilDataBuku();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal Mengahapus Buku!");
                Logger.getLogger(TokoBuku.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_deleteMenuActionPerformed

