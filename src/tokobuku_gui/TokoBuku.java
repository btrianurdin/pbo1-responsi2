    private void tampilDataBuku() {
        model = new DefaultTableModel();
        
        model.addColumn("No.");
        model.addColumn("Kode");
        model.addColumn("Judul");
        model.addColumn("Kategori");
        model.addColumn("Penulis");
        model.addColumn("Tahun");
        model.addColumn("Penerbit");
        model.addColumn("Stok");
        model.addColumn("Harga Beli");
        model.addColumn("Harga Jual");

        try{
            Statement s = (Statement) obj.createStatement();
            ResultSet result = s.executeQuery(
                    "select buku.*,kategori.nama, penulis.nama from buku join kategori on buku.id_kategori = kategori.id"
                            + " join penulis on buku.id_penulis = penulis.id"
            );
            System.out.println(result.getStatement());
            Integer nomor = 1;
            while (result.next()) {
                Object [] ob = new Object[10];
                ob[0] = nomor++;
                ob[1] = result.getString("kode"); 
                ob[2] = result.getString("judul");
                ob[3] = result.getString("kategori.nama");
                ob[4] = result.getString("penulis.nama");
                ob[5] = result.getString("tahun");
                ob[6] = result.getString("penerbit");
                ob[7] = result.getString("stok");
                ob[8] = result.getString("harga_beli");
                ob[9] = result.getString("harga_jual");
                
                model.addRow(ob);
            }
            tabelDataBuku.setModel(model);
            s.close();
            
        } catch (SQLException e) {
            Logger.getLogger(TokoBuku.class.getName()).log(Level.SEVERE, null, e);
        }
    }

